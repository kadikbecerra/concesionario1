	FOR EACH ROW
BEGIN
	INSERT INTO bitacora_empleado(CODEMPLEADO, NOMBRE_EMPLEADO, APELLIDO_EMPLEADO, DIRECCION_EMPLEADO, 
    TELEFONO_EMPLEADO, SEXO_EMPLEADO, SALARIO_EMPLEADO, CI_EMPLEADO, EDAD, EMPLEADO, Movimiento)
